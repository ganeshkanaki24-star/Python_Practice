# List
my_list = [10, 20, 30, 40]
print("List:", my_list)

# Tuple
my_tuple = (1, 2, 3, 4)
print("Tuple:", my_tuple)

# Set
my_set = {100, 200, 300, 400}
print("Set:", my_set)

# Dictionary
my_dict = {"name": "Ganesh", "age": 21, "city": "Pune"}
print("Dictionary:", my_dict)

# Accessing elements
print("\nAccessing Elements")
print("First element of list:", my_list[0])
print("Second element of tuple:", my_tuple[1])
print("Value of name in dictionary:", my_dict["name"])

# Adding elements
my_list.append(50)
my_set.add(500)
my_dict["course"] = "BCA"

print("\nAfter Adding Elements")
print("List:", my_list)
print("Set:", my_set)
print("Dictionary:", my_dict)